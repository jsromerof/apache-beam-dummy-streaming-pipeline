CREATE TABLE CHASSIS (
    ID INT PRIMARY KEY,
    SOURCE_STATUS VARCHAR(100) NOT NULL,
    ARCHIVED_TIME TIMESTAMP(3) WITHOUT TIME ZONE,
    DELETE_TIME TIMESTAMP(3) WITHOUT TIME ZONE
);

CREATE TABLE ENGLISH_STATEMENT (
    ID INT PRIMARY KEY,
    CHASSIS_ID INT REFERENCES CHASSIS(ID),
    SOURCE_STATUS VARCHAR(100) NOT NULL,
    PARENT_ARCHIVED_TIME TIMESTAMP(3) WITHOUT TIME ZONE,
    DELETE_TIME TIMESTAMP(3) WITHOUT TIME ZONE
);

