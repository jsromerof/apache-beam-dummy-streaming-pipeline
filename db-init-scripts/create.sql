    WHERE ID = 1;

    UPDATE ENGLISH_STATEMENT
    SET SOURCE_STATUS = 'ARCHIVED', PARENT_ARCHIVED_TIME = CHASSIS.ARCHIVED_TIME
    FROM CHASSIS
    WHERE ENGLISH_STATEMENT.CHASSIS_ID = CHASSIS.ID AND CHASSIS.ARCHIVED_TIME - ENGLISH_STATEMENT.DELETE_TIME < INTERVAL '1 days'
    AND CHASSIS.ID = 1;

    DELETE FROM ENGLISH_STATEMENT
    WHERE ENGLISH_STATEMENT.CHASSIS_ID = CHASSIS.ID AND CHASSIS.ARCHIVED_TIME - ENGLISH_STATEMENT.DELETE_TIME > INTERVAL '1 days'
    AND CHASSIS.ID = 1;
COMMIT;

